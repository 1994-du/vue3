    <p>7. 闭包可以用来实现惰性函数。</p>
    <p>8. 闭包可以用来实现递归。</p>
    <p>9. 闭包可以用来实现函数的记忆化。</p>
    <p>10. 闭包可以用来实现函数的柯里化。</p>
