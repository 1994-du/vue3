    <h4>什么是闭包？</h4>
    <p>闭包是指有权访问另一个函数作用域中的变量的函数。</p>
    <h4>闭包的关键点</h4>
	<p>1.函数嵌套：闭包通常由一个嵌套函数创建。</p>
	<p>2.访问外部变量：内层函数能够访问其外层函数作用域中的变量。</p>
