    <h4>闭包的作用</h4>
    <p>1. 读取函数内部的变量</p>
    <p>2. 让这些变量的值始终保持在内存中</p>
    <p>3. 封装变量</p>
    <p>4. 延长作用域</p>
    <h4>闭包的缺点</h4>
    <p>1. 闭包会使得函数中的变量都被保存在内存中，内存消耗很大，所以不能滥用闭包，否则会造成网页的性能问题。</p>
    <p>2. 闭包会在父函数外部改变父函数内部变量的值。</p>
    <p>3. 闭包会使得程序的逻辑变得复杂。</p>
    <h4>闭包的应用场景</h4>
